package cn.pojo.mysql;

public class yearpeople {
    public String year;
    public String high;
    public String low;

    public String getYear() {
        return year;
    }

    public String getHigh() {
        return high;
    }

    public String getLow() {
        return low;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public void setHigh(String high) {
        this.high = high;
    }

    public void setLow(String low) {
        this.low = low;
    }
}
